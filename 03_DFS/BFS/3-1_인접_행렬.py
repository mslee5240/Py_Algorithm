# 그래프: 노드(Node) = 정점(Vertex) / 간선(Edge)로 구성

# 인접 행렬(Adjacency Matrix): 2차원 배열로 그래프의 연결 관계를 표현하는 방식
# 인접 리스트(Adjacency List): 리스트로 그래프의 연결 관계를 표현하는 방식

INF = 999999999 # 무한의 비용 선언

# 2차원 리스트를 이용해 인접 행렬 표현
graph = [
    [0, 7, 5], # 0번 노드
    [7, 0, INF], # 1번 노드
    [5, INF, 0] # 2번 노드
]

print(graph)