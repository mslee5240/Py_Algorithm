# sorted() 함수나 list.sort() 메서드에서 "key"는 정렬 기준을 정의하는 함수를 받습니다. 
# 리스트의 각 요소가 하나씩 key로 전달된 함수에 들어가고, 그 함수가 반환한 값을 기준으로 정렬이 이루어집니다.

# 작동 과정
# - 리스트의 각 요소를 순서대로 key 함수에 입력합니다.
# - key 함수가 반환하는 값을 기준으로 리스트를 정렬합니다.
# - 반환된 값들을 비교하여 오름차순(기본) 또는 내림차순(reverse=True)으로 정렬합니다.

array = [(1, 2), (3, 1), (5, 7), (2, 5)]
sorted_array = sorted(array, key=lambda x: x[1])  # 두 번째 요소를 기준으로 정렬
print(sorted_array)